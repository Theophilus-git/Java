public class augmentedAssignment{
    public static void main(String[] args){

        //Augmented Assignment Operations

        int x = 10;
        int y = 3;

        /*x = x + y; Or */ x += y;// This is for additions
        /*x = x * y; Or */ x *= y;//This is for multiplications
        /*x = x - y; Or */ x -= y;//This is for substractions
        /*x = x / y; Or */ x /= y;//This is for Division
        /*x = x % y; Or */ x %= y;//This is for Modulation

        
        System.out.print(x);

    }
}